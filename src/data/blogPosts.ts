export function getBlogPostBySlug(slug: string): BlogPost | undefined {
  return blogPosts.find((p) => p.published && p.slug === slug)
}

export function getPublishedBlogPosts(): BlogPost[] {
  return blogPosts.filter((p) => p.published).sort((a, b) => (b.date > a.date ? 1 : -1))
}
